def calculate_trophic_levels(graph):
    """Вычисляет трофический уровень каждого вида"""
    levels = {}

    # Продуценты (никого не едят)
    producers = [n for n in graph.nodes() if graph.out_degree(n) == 0]

    if not producers:
        min_out = min(graph.out_degree(n) for n in graph.nodes())
        producers = [n for n in graph.nodes() if graph.out_degree(n) == min_out]

    for p in producers:
        levels[p] = 1

    # Распространение уровней
    changed = True
    while changed:
        changed = False
        for predator in graph.nodes():
            if predator in levels:
                continue
            prey_list = list(graph.successors(predator))
            if prey_list:
                prey_levels = [levels[p] for p in prey_list if p in levels]
                if prey_levels:
                    new_level = max(prey_levels) + 1
                    levels[predator] = new_level
                    changed = True

    for node in graph.nodes():
        if node not in levels:
            levels[node] = 2

    return levels



def calculate_biomass(node, trophic_level, graph):

    base_biomass = {
        1: 100.0,   # Продуценты
        2: 20.0,    # Травоядные
        3: 5.0,     # Хищники
        4: 1.0,     # Суперхищники
    }

    biomass = base_biomass.get(trophic_level, 2.0)

    # Корректировка: чем больше врагов (входящих связей), тем меньше биомасса
    num_predators = graph.in_degree(node)
    if num_predators > 0:
        biomass = biomass / (1 + 0.1 * num_predators)

    # Чем больше жертв, тем больше биомасса (хищник сытый)
    num_prey = graph.out_degree(node)
    if num_prey > 0:
        biomass = biomass * (1 + 0.05 * min(num_prey, 5))

    return max(1.0, min(100.0, biomass))


def calculate_energy_flow(predator, prey, interaction_count, trophic_level_predator):
    """
    Расчет относительного потока энергии от жертвы к хищнику.

    Принцип:
    - Травоядные → хищники: средний поток
    - Хищники → суперхищники: меньший поток (потери энергии)
    - Чем чаще встречается пара, тем больше поток
    """
    base_flow = 10.0

    # Потери энергии на каждом трофическом уровне (~90% теряется)
    efficiency = 0.1  # 10% эффективность передачи энергии
    level_loss = efficiency ** (trophic_level_predator - 1)

    flow = base_flow * level_loss

    # Корректировка по частоте взаимодействий
    flow = flow * (1 + 0.1 * min(interaction_count, 10))

    return max(1.0, min(20.0, flow))

