# --- ГЕНЕРАТОР ---
def build_generator():
    model = Sequential([
        Dense(128 * 8 * 8, input_dim=LATENT_DIM, use_bias=False),
        BatchNormalization(),
        LeakyReLU(alpha=0.2),
        Reshape((8, 8, 128)),

        Conv2DTranspose(64, (4, 4), strides=(2, 2), padding='same', use_bias=False),
        BatchNormalization(),
        LeakyReLU(alpha=0.2),

        Conv2DTranspose(32, (4, 4), strides=(2, 2), padding='same', use_bias=False),
        BatchNormalization(),
        LeakyReLU(alpha=0.2),

        Conv2DTranspose(16, (4, 4), strides=(2, 2), padding='same', use_bias=False),
        BatchNormalization(),
        LeakyReLU(alpha=0.2),

        Conv2DTranspose(3, (4, 4), strides=(1, 1), padding='same', use_bias=False, activation='tanh')
    ])
    return model

# --- ДИСКРИМИНАТОР ---
def build_discriminator():
    model = Sequential([
        Conv2D(64, (4, 4), strides=(2, 2), padding='same', input_shape=[IMG_SIZE, IMG_SIZE, 3]),
        LeakyReLU(alpha=0.2),
        Dropout(0.3),

        Conv2D(128, (4, 4), strides=(2, 2), padding='same'),
        BatchNormalization(),
        LeakyReLU(alpha=0.2),
        Dropout(0.3),

        Conv2D(256, (4, 4), strides=(2, 2), padding='same'),
        BatchNormalization(),
        LeakyReLU(alpha=0.2),
        Dropout(0.3),

        Flatten(),
        Dense(1)
    ])
    return model

