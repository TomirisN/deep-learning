    analysis = {
        "errors": [],
        "limitations": [],
        "strengths": []
    }

    # Проверка наличия изображений
    real_count = len(glob.glob(f"{real_images_path}/*.png"))
    gen_count = len(glob.glob(f"{generated_images_path}/*.png")) if os.path.exists(generated_images_path) else 0

