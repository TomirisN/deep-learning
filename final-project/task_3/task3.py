def calculate_fid(real_images_path, generated_images_path, model_path=None, max_images=500):
    """
    Рассчитывает FID между реальными и сгенерированными изображениями
    FID < 50 - отлично, 50-100 - хорошо, >200 - плохо
    """

    print("\n" + "="*60)
    print("1. РАСЧЕТ FID (Fréchet Inception Distance)")
    print("="*60)

    # Загрузка InceptionV3 для извлечения признаков
    inception = InceptionV3(include_top=False, pooling='avg', input_shape=(299, 299, 3))

    def load_images_from_folder(folder_path, max_img=500, is_generated=False):
        """Загружает и подготавливает изображения"""
        image_paths = glob.glob(f"{folder_path}/*.png")
        if len(image_paths) == 0:
            print(f"  Папка {folder_path} пуста!")
            return None

        images = []
        for path in image_paths[:max_img]:
            try:
                img = Image.open(path).convert('RGB')
                img = img.resize((299, 299))
                img = np.array(img)
                img = preprocess_input(img)
                images.append(img)
            except Exception as e:
                print(f"  Ошибка с {path}: {e}")

        print(f"  Загружено {len(images)} изображений из {folder_path}")
        return np.array(images)

    def get_features(images, model, batch_size=50):
        """Извлекает признаки через InceptionV3"""
        features = []
        for i in range(0, len(images), batch_size):
            batch = images[i:i+batch_size]
            batch_features = model.predict(batch, verbose=0)
            features.append(batch_features)
        return np.vstack(features)

    # Загрузка реальных изображений
    print("\n Загрузка реальных изображений...")
    real_images = load_images_from_folder(real_images_path, max_images)
    if real_images is None:
        return None

    # Загрузка или генерация сгенерированных изображений
    print("\n Загрузка сгенерированных изображений...")
    if os.path.exists(generated_images_path) and len(glob.glob(f"{generated_images_path}/*.png")) > 0:
        gen_images = load_images_from_folder(generated_images_path, max_images)
    elif model_path and os.path.exists(model_path):
        print("  Генерация изображений из модели...")
        generator = tf.keras.models.load_model(model_path)
        noise = tf.random.normal([max_images, 128])
        gen_imgs = generator(noise, training=False).numpy()
        gen_imgs = (gen_imgs + 1) / 2
        gen_images = []
        for img in gen_imgs:
            img_uint8 = (img * 255).astype(np.uint8)
            img_resized = np.array(Image.fromarray(img_uint8).resize((299, 299)))
            gen_images.append(preprocess_input(img_resized))
        gen_images = np.array(gen_images)
        print(f"  Сгенерировано {len(gen_images)} изображений")
    else:
        print("  Нет сгенерированных изображений и модели!")
        return None

    # Извлечение признаков
    print("\n🔍 Извлечение признаков через InceptionV3...")
    real_features = get_features(real_images, inception)
    gen_features = get_features(gen_images, inception)

    # Расчет FID
    mu_real, sigma_real = real_features.mean(axis=0), np.cov(real_features, rowvar=False)
    mu_gen, sigma_gen = gen_features.mean(axis=0), np.cov(gen_features, rowvar=False)

    diff = mu_real - mu_gen
    mean_diff = np.dot(diff, diff)

    covmean = sqrtm(sigma_real.dot(sigma_gen))
    if np.iscomplexobj(covmean):
        covmean = covmean.real

    fid = mean_diff + np.trace(sigma_real + sigma_gen - 2 * covmean)

    return fid


